-- Spell Correction Dictionary
-- Supports production-grade spell correction with word frequencies

-- SQLite version
-- Table: spell_dictionary
CREATE TABLE IF NOT EXISTS spell_dictionary (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    word TEXT NOT NULL COLLATE NOCASE,
    frequency INTEGER NOT NULL DEFAULT 1,
    is_builtin BOOLEAN NOT NULL DEFAULT 0,
    source TEXT DEFAULT 'indexed',
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UNIQUE(word)
);

-- Indexes for fast lookup
CREATE INDEX IF NOT EXISTS idx_spell_dictionary_word ON spell_dictionary(word);
CREATE INDEX IF NOT EXISTS idx_spell_dictionary_frequency ON spell_dictionary(frequency DESC);
CREATE INDEX IF NOT EXISTS idx_spell_dictionary_source ON spell_dictionary(source);

